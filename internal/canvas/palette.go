package canvas

import (
	"image/color"
)

// Color returns the color at index n.
func Color(p color.Palette, n int) color.NRGBA {
	if n < 0 {
		return color.NRGBA{}
	}

	c := p[n%len(p)]
	return c.(color.NRGBA)
}

// doodleKitPalette is the palette used by DoodleKit.
func doodleKitPalette() color.Palette {
	return color.Palette{
		color.NRGBA{0xFF, 0xFF, 0xFF, 0x00}, // transparent
		color.NRGBA{0x00, 0x00, 0x00, 0xFF}, // black
		color.NRGBA{0x1D, 0x2B, 0x53, 0xFF}, // dark-blue
		color.NRGBA{0x7E, 0x25, 0x53, 0xFF}, // dark-purple
		color.NRGBA{0x00, 0x87, 0x51, 0xFF}, // dark-green
		color.NRGBA{0xAB, 0x52, 0x36, 0xFF}, // brown
		color.NRGBA{0x5F, 0x57, 0x4F, 0xFF}, // dark-grey
		color.NRGBA{0xC2, 0xC3, 0xC7, 0xFF}, // light-grey
		color.NRGBA{0xFF, 0xF1, 0xE8, 0xFF}, // white
		color.NRGBA{0xFF, 0x00, 0x4D, 0xFF}, // red
		color.NRGBA{0xFF, 0xA3, 0x00, 0xFF}, // orange
		color.NRGBA{0xFF, 0xEC, 0x27, 0xFF}, // yellow
		color.NRGBA{0x00, 0xE4, 0x36, 0xFF}, // green
		color.NRGBA{0x29, 0xAD, 0xFF, 0xFF}, // blue
		color.NRGBA{0x83, 0x76, 0x9C, 0xFF}, // lavender
		color.NRGBA{0xFF, 0x77, 0xA8, 0xFF}, // pink
		color.NRGBA{0xFF, 0xCC, 0xAA, 0xFF}, // light-peach
	}
}
